import math
ladoa=float(input("Ingrese el lado a: "))
ladob=float(input("Ingrese el lado b: "))
ladoc=math.sqrt(math.pow(ladoa,2)+math.pow(ladob,2))
print("El lado c del triangulo es: ",round(ladoc,2))
